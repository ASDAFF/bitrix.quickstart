<p><strong>Обязанности дежурного по классу</strong> </p>
 
<ol> 
  <li>Дежурные назначаются в соответствии с графиком дежурства по классу.</li>
 Дежурные помогают педагогу подготовить класс для следующего урока, производят посильную уборку классного помещения. 
  <li>Во время перемены дежурный ученик (ученики) проветривает класс, помогает учителю развесить учебный материал для следующего урока, раздает тетради по просьбе учителя.</li>
 
  <li>В конце рабочего дня дежурные учащиеся готовят класс для следующего рабочего дня (протирают пыль с мебели, полы, поливают цветы).</li>
 
  <li>Дежурные 1-3-х классов осуществляют посильную уборку (протирают парты, поливают цветы, выносят мусор). </li>
 </ol>
 
