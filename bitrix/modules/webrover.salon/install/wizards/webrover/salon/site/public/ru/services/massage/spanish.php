<p>Техника испанского массажа основана на трех основных постулатах, сформулированных Э. Гарсия. Во-первых, в массаже не допускается использование двух техник, так как некоторые из них могут взаимоисключать друг друга, обладая противоположным действием. Во-вторых, в технику испанского массажа входит более сотки различных приемов. При подобном разнообразии учитываются индивидуальные особенности организма пациента, а также исключается привыкание к повторяющимся приемам, что значительно снижает их эффективность. В-третьих, данный массаж отличается невероятной пластичностью приемов, тем самым исключаются болезненные ощущения (при этом массаж обеспечивает глубокое проникновение в ткани). Гарсия в данном случае подчеркивает важность оздоровительного эффекта эндоморфинов, вызывающих при их появлении в организме чувства радости и удовольствия.</p>
