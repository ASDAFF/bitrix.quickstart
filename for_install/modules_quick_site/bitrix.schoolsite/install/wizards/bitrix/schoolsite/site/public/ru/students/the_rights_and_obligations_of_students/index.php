<p> <strong>Поведение на занятиях</strong> </p>
 
<ol> 
  <li>Не опаздывать на урок. Когда учитель входит в класс, учащиеся встают, приветствуя учителя. Подобным образом учащиеся приветствуют любого взрослого, вошедшего в класс во время занятий (кроме уроков информатики в компьютерном классе).</li>
 
  <li>Каждый учитель определяет правила поведения учащихся на своих занятиях; эти правила не должны ущемлять достоинство ученика и противоречить Уставу школы.</li>
 
  <li>о время, урока нельзя шуметь, отвлекаться самому и отвлекать других товарищей от занятий посторонними разговорами, играми и другими, не относящимися к уроку делами.</li>
 
  <li>Если во время занятий учащемуся необходимо выйти из класса, то он должен попросить разрешения педагога.</li>
 
  <li>Если учащийся хочет задать вопрос учителю или ответить на вопрос учителя, он поднимает руку.</li>
 
  <li>Во время урока ученик вправе задавать вопросы учителю, если не понял материал во время объяснения.</li>
 
  <li>Ученик вправе отстаивать свой взгляд и свои убеждения при обсуждении различных спорных и неоднозначных вопросов (соблюдая корректную форму).</li>
 
  <li>На уроках учащиеся имеют право пользоваться школьным инвентарем, который они возвращают учителю после занятия. Относиться к нему следует бережно и аккуратно.</li>
 
  <li>Ученики не должны разговаривать на посторонние темы на уроках, так как они этим нарушают права других на получение необходимых знаний. </li>
 </ol>
 
