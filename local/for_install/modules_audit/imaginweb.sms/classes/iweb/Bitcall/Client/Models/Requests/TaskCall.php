namespace Bitcall\Client\Models\Requests;


abstract class TaskCall {
    private $phone;
    private $id;

    function __construct($phone, $id = null)
    {
        $this->id = $id;
        $this->phone = $phone;
    }


    /**
     * @param string $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return string
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param string $phone
     */
    public function setPhone($phone)
    {
        $this->phone = $phone;
    }

    /**
     * @return string
     */
    public function getPhone()
    {
        return $this->phone;
    }

}