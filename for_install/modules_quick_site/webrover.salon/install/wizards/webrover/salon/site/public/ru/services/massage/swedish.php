<p>Методика шведского массажа включает в себя следующие приемы: поглаживания, растирания, разминания и движения, причем акцент делается именно на растирания и движения. Целью шведского массажа является глубокое проникновение в ткани, растирание образовавшихся уплотнений и растягивание сосудисто-нервных пучков. Массаж выполняется в направлении от периферии к центру (стопа - голень - бедро, кисть - предплечье &ndash; плечо). В результате проведения цикла шведского массажа восстанавливается гибкость суставов, и исчезают отеки.</p>
 
