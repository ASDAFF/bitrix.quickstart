<p><strong>Поведение учащихся в столовой</strong> </p>
 
<ol> 
  <li>Во время приема пищи в столовой учащимся надлежит придерживаться хороших манер и вести себя пристойно.</li>
 
  <li>Учащиеся должны уважительно относиться к работникам столовой.</li>
 
  <li>Разговаривать во время еды следует не громко, чтобы не беспокоить окружающих.</li>
 
  <li>Учащиеся убирают за собой посуду после принятия пищи, ставят на место стулья.</li>
 
  <li>Учащиеся бережно относятся к имуществу школьной столовой.</li>
 
  <li>Запрещается приходить в столовую в верхней одежде.</li>
 
  <li>Запрещается выносить еду из столовой. Купленную в школьном буфете выпечку, соки и другие продукты надо есть в столовой (закрытые упаковки можно оставить до следующей перемены).</li>
 
  <li>Учащиеся имеют право принести в столовую принесенный из дома завтрак.</li>
 
  <li>Дежурные по столовой помогают обслуживающему персоналу накрывать столы, убирать посуду со столов, вытирают столы, расставляют на место стулья. </li>
 </ol>
 
