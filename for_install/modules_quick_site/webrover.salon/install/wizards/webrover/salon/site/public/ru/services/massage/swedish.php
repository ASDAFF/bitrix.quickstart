<p>Важно отметить также психотерапевтические свойства данной системы массажа. Массаж проводится в медленном темпе, и воздействие оказывается непосредственно на нервные окончания, что способствует полному расслаблению пациента, снятию психического напряжения. Как правило, пациент засыпает спустя 40 минут (глубокий сон пациента не следует прерывать в течение минимум 15 минут). Таким образом, данный вид массажа успешно используется в качестве средства против различных невротических состояний, усталости и стресса.</p>
 
