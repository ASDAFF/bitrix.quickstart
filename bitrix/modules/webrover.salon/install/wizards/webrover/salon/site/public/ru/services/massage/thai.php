<p>Тайский массаж проводится в теплом, хорошо проветриваемом помещении (в полной тишине или под тихую музыку для релаксации). Массаж проводится на полу, клиент располагается на мягком и упругом коврике, под голову подкладывается специальная подушка. Клиент во время массажа не раздевается, его одежда должна быть из тонкого натурального материала и достаточно свободна (обнажены лишь стопы). На первом этапе массажа клиент лежит на спине, потом он переворачивается на живот; заканчивается процедура массажа в положении сидя.</p>
 
