<p><strong>Обязанности дежурного класса</strong> </p>
 
<ol> 
  <li>Дежурство по школе осуществляется с помощью учащихся 8-11 классов; в 4 четверти от дежурства освобождаются 9 и 11 классы; каждый класс дежурит по одной неделе по очереди или по составленному графику.</li>
 
  <li>Дежурному обязательно ношение повязки на левом рукаве или ношение бейджика.</li>
 
  <li>График дежурства по школе разрабатывается заместителем директора по воспитательной работе совместно с классными руководителями.</li>
 
  <li>Учащиеся дежурного класса вместе с классным руководителем приходят в школу в 8.00 и уходят домой после 7-го урока.</li>
 
  <li>Дежурные в раздевалке и столовой дежурят до 15.00.</li>
 
  <li>Дежурные на входной двери проверяют наличие сменной обуви.</li>
 
  <li>Дежурные учащиеся помогают младшим школьникам переодеться, на своих постах следят за соблюдением дисциплины и порядка во время перемен, замечания нарушителям делаются в тактичной форме (в случае непонимания обращаются к дежурному учителю, классному руководителю за помощью).</li>
 
  <li>Оказывают необходимую помощь в организации учебно-воспитательного процесса учителям и администрации школы.</li>
 
  <li>В течение всего дежурства класса ведется «журнал дежурства». В журнал записываются замечания, пожелания и др. (фамилии опоздавших учащихся и часто нарушающих дисциплину, и т.п.).</li>
 
  <li>В последний день дежурства проводится линейка, на которой передаются «полномочия» дежурного класса (повязки, журнал дежурства) другому классу. </li>
 </ol>
 
