CREATE TABLE IF NOT EXISTS `api_qa_question` (
	`ID` INT(18) NOT NULL AUTO_INCREMENT,
	`DATE_CREATE` DATETIME NULL DEFAULT NULL,
	`ACTIVE` CHAR(1) NOT NULL DEFAULT 'Y',
	`TYPE` CHAR(1) NULL DEFAULT 'Q',
	`PARENT_ID` INT(18) NULL DEFAULT '0',
	`LEVEL` INT(18) NULL DEFAULT '0',
	`IBLOCK_ID` INT(18) NULL DEFAULT '0',
	`ELEMENT_ID` INT(18) NULL DEFAULT NULL,
	`XML_ID` VARCHAR(255) NULL DEFAULT NULL,
	`CODE` VARCHAR(255) NULL DEFAULT NULL,
	`USER_ID` INT(18) NULL DEFAULT NULL,
	`VOTE_UP` INT(11) NULL DEFAULT '0',
	`VOTE_DO` INT(11) NULL DEFAULT '0',
	`LOCATION` VARCHAR(255) NULL DEFAULT NULL,
	`NOTIFY` CHAR(1) NOT NULL DEFAULT 'Y',
	`GUEST_NAME` VARCHAR(255) NULL DEFAULT NULL,
	`GUEST_EMAIL` VARCHAR(255) NULL DEFAULT NULL,
  `PAGE_TITLE` VARCHAR(255) NULL DEFAULT NULL,
  `PAGE_URL` VARCHAR(255) NULL DEFAULT NULL,
  `TEXT` TEXT NULL,
	`IP` VARCHAR(15) NULL DEFAULT NULL,
	`SITE_ID` CHAR(2) NULL DEFAULT NULL,
	PRIMARY KEY (`ID`),
	INDEX `ELEMENT_ID` (`ELEMENT_ID`),
	INDEX `USER_ID` (`USER_ID`),
	INDEX `IBLOCK_ID` (`IBLOCK_ID`),
	INDEX `XML_ID` (`XML_ID`),
	INDEX `CODE` (`CODE`)
);