
<p>Клиника создана более 15 лет назад и функционирует на основании Устава и в соответствии с лицензией Департамента Здравоохранения г. Москвы.</p>
 
<p>Клиника осуществляет комплексную диагностику и эффективное лечение заболеваний внутренних органов, не требующих хирургического вмешательства. Клиника оснащена современным диагностическим и терапевтическим оборудованием.</p>
