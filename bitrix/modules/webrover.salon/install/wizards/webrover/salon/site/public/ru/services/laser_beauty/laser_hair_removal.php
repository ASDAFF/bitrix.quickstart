<p>Сущность лазерной эпиляции &ndash; метода, позволяющего избавиться от нежелательных волос - заключается в воздействии лазера на репродуктивную систему волос &ndash; волосяные фолликулы, являющиеся источником роста волос.</p>
 
<p>Лазеры, используемые в косметологии, обладают свойством мгновенно нагревать, а затем разрушать клетки, содержащие пигмент (меланин). Лазерный луч пробегает от кончика волоса до его репродуктивной системы &ndash; волосяной луковицы и быстро уничтожает ее. При этом разрушение волосяного фолликула происходит за миллисекунды. Кожа сильно нагревается, что доставляет временный дискомфорт, для уменьшения которого используется охлаждение. Исключительно точечное воздействие является результатом действия лазера только на клетки, содержащие значительное количество пигмента. Это клетки, составляющие волосяную луковицу. К сожалению, достоинства лазера являются прямым продолжением его недостатков. Лазер не действует на светлые волосы и неэффективен при темной коже.</p>
 
