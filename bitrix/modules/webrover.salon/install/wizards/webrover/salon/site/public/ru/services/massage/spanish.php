<p>Испанский массаж включает в себя несколько уникальных техник. Миотенсивная техника включает в себя расслабляющие воздействия на каждую группу мышц, в процессе которого сустав приводится в действие и прорабатывается сочленением. Подобная техника направлена на восстановления эластичности мышечно-суставного сочленения.</p>
 <p>Соматоэмоцнональная техника направлена на укрепление нервной системы и построена на контрасте ощущений: от расслабляющих приемов до достаточно усиленных действий для раздражения рефлекторных зон (движения выполняются ладонью или пальцами в разных направлениях, с разной скоростью и силой давления). Иногда используются дополнительные материалы: камни или перья. Еще одна техника &ndash; лимфодренаж &ndash; направлена на удаление из тканей избытка жидкостей и продуктов обмена. Гемолимфатическая техника направлена на нормализацию местного кровообращения, снижение артериального и внутречерепного давления, а нейроссдативная техника массажа снимает стресс, апатию, раздражительность, нарушения сна и заключается в стимуляции определенных мозговых центров (подобный массаж вызывает ощущения, схожие с теми&raquo; которые испытывает человек во внутриутробном периоде). Сеансы испанского массажа можно проводить не чаще 3 раз в неделю.</p>
 
 
