<p>Массаж начинается со стоп, с их растягивания и вращения. Существует около сотки приемов тайского массажа, и каждый массажист придерживается какого-либо своего порядка. Основными приемами тайского массажа являются надавливание (большим пальцем, ладонью, локтем, коленом, ступней или ягодицами) и манипуляция, то есть движение, придаваемое какой-либо части тела с целью растягивания или скручивания. К манипуляции относится растягивание конечностей, подъем, встряхивание, вращение, вытягивание с толканием. Объектом надавливания или манипуляции может стать практически любая часть туловища.</p>
 
<p>В общей сложности массаж занимает около двух часов. При проведении массажа необходимо избегать чрезмерного растягивания мышц, следует помнить, что у каждого человека свой болевой порог. Главными принципами тайского массажа в любом случае является мягкость и ритмичность. На счет дыхания клиента определенных правил не существует, клиент должен дышать произвольно за исключением выполнения приема &laquo;Кобра&raquo; и подъема со скрученной спиной.</p>
 
