<h3>2.Интрадермальные образования:</h3>
 
<ul> 
  <li>дерматофибромы - плотные узловые образования со стягиванием кожи к центру и пигментацией различной степени над узелками от розовой до сине-коричневой. Существует небольшая вероятность их перерождения;</li>
 
  <li>интрадермальные пигментные невусы - плоские мягкоэластические образования в виде пятен на коже от светло- до темно-коричневого цвета;</li>
 
