CREATE TABLE IF NOT EXISTS `api_reviews` (
  `ID` INT(18) NOT NULL AUTO_INCREMENT,
  `TIMESTAMP_X` DATETIME NULL DEFAULT NULL,
  `ACTIVE_FROM` DATETIME NULL DEFAULT NULL,
  `DATE_CREATE` DATETIME NULL DEFAULT NULL,
  `ACTIVE` CHAR(1) NULL DEFAULT 'Y',
  `RATING` INT(1) NULL DEFAULT '0',
  `THUMBS_UP` INT(11) NULL DEFAULT '0',
  `THUMBS_DOWN` INT(11) NULL DEFAULT '0',
  `SITE_ID` CHAR(2) NULL DEFAULT 's1',
  `USER_ID` INT(18) NULL DEFAULT NULL,
  `IBLOCK_ID` INT(11) NULL DEFAULT NULL,
  `SECTION_ID` INT(11) NULL DEFAULT NULL,
  `ELEMENT_ID` INT(11) NULL DEFAULT NULL,
  `ORDER_ID` VARCHAR(255) NULL DEFAULT NULL,
  `URL` TEXT NULL DEFAULT NULL,
  `DELIVERY` INT(11) NULL DEFAULT NULL,
  `CITY` VARCHAR(255) NULL DEFAULT NULL,
  `GUEST_NAME` VARCHAR(255) NULL DEFAULT NULL,
  `GUEST_EMAIL` VARCHAR(255) NULL DEFAULT NULL,
  `GUEST_PHONE` VARCHAR(255) NULL DEFAULT NULL,
  `TITLE` VARCHAR(255) NULL DEFAULT NULL,
  `COMPANY` VARCHAR(255) NULL DEFAULT NULL,
  `WEBSITE` VARCHAR(255) NULL DEFAULT NULL,
  `ADVANTAGE` TEXT NULL DEFAULT NULL,
  `DISADVANTAGE` TEXT NULL DEFAULT NULL,
  `ANNOTATION` TEXT NULL DEFAULT NULL,
  `REPLY` TEXT NULL DEFAULT NULL,
  `PAGE_URL` TEXT NULL DEFAULT NULL,
  `PAGE_TITLE` VARCHAR(255) NULL DEFAULT NULL,
  `REPLY_SEND` CHAR(1) NULL DEFAULT 'N',
  `SUBSCRIBE_SEND` CHAR(1) NULL DEFAULT 'N',
  `EULA_ACCEPTED` CHAR(1) NULL DEFAULT 'Y',
  `PRIVACY_ACCEPTED` CHAR(1) NULL DEFAULT 'Y',
  `FILES` TEXT NULL DEFAULT NULL,
  `VIDEOS` TEXT NULL DEFAULT NULL,
  `IP` VARCHAR(15) NULL DEFAULT NULL,
  PRIMARY KEY (`ID`),
  INDEX `SECTION_ID` (`SECTION_ID`),
  INDEX `ELEMENT_ID` (`ELEMENT_ID`),
  INDEX `USER_ID` (`USER_ID`),
  INDEX `ORDER_ID` (`ORDER_ID`),
  INDEX `IBLOCK_ID` (`IBLOCK_ID`)
);
CREATE TABLE IF NOT EXISTS `api_reviews_agent` (
	`ID` INT(18) NOT NULL AUTO_INCREMENT,
	`DATE_INSERT` DATETIME NULL DEFAULT NULL,
	`DATE_EXEC` DATETIME NULL DEFAULT NULL,
	`REVIEW_ID` INT(18) NULL DEFAULT NULL,
	`SUBSCRIBERS_CNT` INT(18) NULL DEFAULT NULL,
	`SENDMAIL_CNT` INT(18) NULL DEFAULT NULL,
	`SITE_ID` CHAR(2) NULL DEFAULT NULL,
	PRIMARY KEY (`ID`),
	INDEX `DATE_EXEC` (`DATE_EXEC`),
	INDEX `REVIEW_ID` (`REVIEW_ID`)
);
CREATE TABLE IF NOT EXISTS `api_reviews_subscribe` (
	`ID` INT(18) NOT NULL AUTO_INCREMENT,
	`DATE_INSERT` DATETIME NOT NULL,
	`EMAIL` VARCHAR(255) NULL DEFAULT NULL,
	`USER_ID` INT(11) NULL DEFAULT NULL,
	`IBLOCK_ID` INT(18) NULL DEFAULT NULL,
	`SECTION_ID` INT(18) NULL DEFAULT NULL,
	`ELEMENT_ID` INT(18) NULL DEFAULT NULL,
	`URL` TEXT NULL,
	`SITE_ID` CHAR(2) NULL DEFAULT NULL,
	PRIMARY KEY (`ID`),
	INDEX `EMAIL` (`EMAIL`),
	INDEX `IBLOCK_ID` (`IBLOCK_ID`),
	INDEX `SECTION_ID` (`SECTION_ID`),
	INDEX `ELEMENT_ID` (`ELEMENT_ID`)
);
CREATE TABLE IF NOT EXISTS `api_reviews_video` (
  `ID` INT(18) NOT NULL AUTO_INCREMENT,
  `CODE` VARCHAR(255) NULL DEFAULT NULL,
  `SERVICE` VARCHAR(255) NULL DEFAULT NULL,
  `FILE_ID` INT(18) NULL DEFAULT NULL,
  `TITLE` VARCHAR(255) NULL DEFAULT NULL,
  `DESCRIPTION` LONGTEXT NULL,
  PRIMARY KEY (`ID`),
  INDEX `VIDEO_ID` (`CODE`),
  INDEX `SERVICE` (`SERVICE`),
  INDEX `FILE_ID` (`FILE_ID`)
);
