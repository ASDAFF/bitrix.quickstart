 </ul>
 
<h3>3.Экзофитные образования</h3>
 
<ul> 
  <li>папилломы - мягкоэластические образования на узком основании в виде столбиков, имеющие цвет кожи или слабопигментированные;</li>
 
  <li>кератомы - плотные, шелушащиеся образования в виде скопления чешуек кожи, слоистые, серого, желто-серого или серо-коричневого оттенка; в зоне их расположения наблюдается характерный зуд кожи;</li>
 
  <li>мягкие фибромы или фибролипомы - мягкоэластические образования в виде мешочков с жировой клетчаткой и соединительной тканью, имеющие цвет кожи; могут быть как небольших размеров (до 0,5 см), так и огромные, достигая иногда размеров головы ребенка;</li>
 
  <li>соединительно-тканные невусы или фибропапилломы - плотноэластические образования цвета кожи, возвышающиеся над ней в виде полусфер;</li>
 
  <li>папилломатозные невусы - мягкоэластические образования, напоминающие цветную капусту, цвета кожи или слегка пигментированные; на поверхности могут иметь более плотные островки ороговения;</li>
 
  <li>пигментные невусы - мягкоэластические образования каплевидной формы, имеющие цвет от светло- до темно-коричневого и возвышающиеся над коже;</li>
 </ul>
 
<h3>4.Смешанные образования:</h3>
 
<ul> 
